package util;

public class Person {
	
	private final int id = 0;
	private final String name = "";
	private final String number = "";
	
	public String getName()
	{
		return name;
	}
	
	public String getNum()
	{
		return number;
	}
	
	public int getId()
	{
		return id;
	}
	
	public String toString()
	{
		return "Name: " + name + "Number: " + number;
	}

}
